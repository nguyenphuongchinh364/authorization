const express = require('express');
const router = express.Router();
const roleController = require('../controllers/roleController');
const auth = require('../middlewares/authMiddleware');
const checkRole = require('../middlewares/checkroleMiddleware');

router.get("/read/allRoles", auth, checkRole, roleController.getAllRoles);

// Route để tạo mới vai trò
router.post('/create', auth, checkRole, roleController.createRole);

// Route để lấy thông tin vai trò theo ID
router.get('/read/:id', auth, checkRole, roleController.getRoleById);

// Route để cập nhật thông tin vai trò theo ID
router.put('/update/:id', auth, checkRole, roleController.updateRoleById);

// Route để xóa vai trò theo ID
router.delete('/delete/:id', auth, checkRole, roleController.deleteRoleById);

module.exports = router;
